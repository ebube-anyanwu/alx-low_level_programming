#include "3-calc.h"

/**
 * op_add - function that returns the sum of two integers
 * @a: param1
 * @b: param2
 * Return: the sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that returns the difference between two
 * integers
 * @a: param1
 * @b: param2
 * Return: the difference between a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that returns the product of two integers
 * @a: param1
 * @b: param2
 * Return: the product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that returns the result of the
 * division of two integers
 * @a: param1
 * @b: param2
 * Return: the result of the division of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that returns th remainder of the division
 * of two integers
 * @a: param1
 * @b: param2
 * Return: the remainder of the division of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
